    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $duplicates = Cart::search(function ($cartItem, $rowId) use ($request) {
            return $cartItem->id === $request->id;
        });
        if ($duplicates->isNotEmpty()) {
            return redirect()->route('cart.index')->with(['warning'=> 'Item is already in your cart!','title' =>'Already In Cart']);
        }

        //dd($request->image);


        Cart::add(array('id'=>$request->id, 'name'=>$request->name, 'qty'=>$request->qty, 'price'=>$request->price, 'options' => ['image' => $request->image]))
            ->associate('App\Product');
            // dd(Cart::content());
         //return redirect()->route('cart.index')->with(['success_message' => 'Item was added to your cart!']);
         return back()->with(['success' => 'Item was added to your cart!','title'=>'Add To Cart']);
    }

